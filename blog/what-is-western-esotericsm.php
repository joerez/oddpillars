      <div class="container-fluid">
          <p>
            
            Esototericism is the state or quality of being esoteric.
             Generally, in the occult community, when somebody refers to something as being esoteric, they are
            talking about an occult science that's veiled in allegories and symbols, that is intended for only a select few to understand, and has several different layers of meaning.
             It's antonym, exoteric is the surface level message that is in plain view.
          </p>

          <p>
            If you've ever taken a literary English class, you're probably already familiar with this. Authors will convey messages and ideas through metaphors and symbols. For instance, an Author might decide to make something the color black, in order to associate it with the astrological properties of Saturn.
            Upon looking for the hidden meaning behind symbols, a problem arises. It's hard to tell what's intentional and what isn't. In other words, sometimes the curtains are red not for any symbolical meaning, but are red just because the author wanted them to be red.
            It's because of this that the aspiring occult student faces a daunting task. This task involves the constant persuit of trying to figuring out what the intended messages are in ancient mythology and mysticism.
          </p>

          <p>
            It can be a difficult persuit trying to figure out what's esoteric and what is not. Luckily for us, the Western Mystery Tradition's most prominent philosophers have paved the path for us.
            With aid of hermeticism, qabbalah, and other occult philosophical concepts, we are able to decipher hidden meanings from ancient mythology and mysticism.
          </p>
