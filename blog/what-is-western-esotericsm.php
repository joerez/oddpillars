        <p>
          Kabalah, Qabalah, Cabala, why so many different spellings? Since Kabalah is used in so many different spiritual systems in different ways, we distinguish how it's used by the first letter it's spelt with. A 'K' is indicative of the Jewish practice, a 'C' for the Christian, and a 'Q' for the occult and hermetic practice.
        </p>

        <p>
          The Western Esoteric Tradition makes use of Qabalah by way of the Tree of Life, the Tarot, and gemetria.
        </p>

        <p>
          The tree of life is divided into 10 different spheres called Sephiroth, with a hidden eleventh sephiroth called Da'ath which is normally not depicted. The Tree of Life is a map of the Universe. The Tree of Life is another great example of the Macrocasm and Microcasm, and that by studying the above, we can learn about that which is below, and vice versa.
        </p>

        <p>
          Gemetria is the alphanumeric cipher with the belief that words or phrases with identical numerical values bear some relation to each other or bear some relation to the number itself. In Hebrew, each letter also has a numerical value. When Hebrew words are reduced to their numerical values, we find that quite often different words or phrases that are related to each other share the same numerical value. This is also why Qabalah in Hebrew means accounting.
        </p>

        <p>
          The last component of the Western Esoteric Tradition is the study of magic. The magic of Western Esotericism is known as ceremonial magick. Ceremonial magic or ritual magic, also referred to as high magic and as learned magic in some cases, is a broad term used in the context of Hermeticism or Western esotericism to encompass a wide variety of long, elaborate, and complex rituals of magic. It is named as such because the works included are characterized by ceremony and a myriad of necessary accessories to aid the practitioner. It can be seen as an extension of ritual magic, and in most cases synonymous with it. Popularized by the Hermetic Order of the Golden Dawn, it draws on such schools of philosophical and occult thought as Hermetic Qabalah, Enochian magic, Thelema, and the magic of various grimoires.
        </p>

        <p>
          Ceremonial Magic differs from Witchcraft in many different ways. The first of which is that most practitioners of Witchcraft are not well versed in the Western Esoteric Tradition, and many have never heard of it's existence. Witchcraft doesn't require a strong basis of philosophical and occult philosophy to do. Moreover, most Ceremonial Magicians have a degree of skepticism in their practice. Generally speaking, the Ceremonial Magician is more concerned in the persuit of hidden knowledge and the scientific explantions for why things occur than those that practice Witchcraft.
        </p>

