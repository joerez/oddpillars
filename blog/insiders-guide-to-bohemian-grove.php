          <p>
            Let me explain, every decade or so, the Bohemian Club publishes a yearbook. These yearbooks are made specifically for Bohemian Club Members, and are not distributed or sold to the public. The Bohemian Club has been around for such a long time that many of it's older members have passed away, leaving behind all sorts of club memorobalia. Often whoever they leave in charge of their estates have no idea what these books are, so they'll sell them at Garage Sales or book stores. There is still a lack of information regarding this topic, as these books are quite rare to come by. This article will expose the membership process, as outlined in the 6th Volume of the Annals of the Bohemian Club.
          </p>

          <b>Membership Committee</b>

          <p>
            This information comes from Bohemian David M. Atcheson's 6th volume essay.
          </p>

          <p>
            Getting into the Bohemian Club is not as mysterious as it may seem. The Membership Committee is an admissions committee which screens and processes candidates but generally plays no continueing role in a Bohemian's life once he becomes a member. The committee does not establish the number of members nor membership categories, nor does it recruit members. Sponsors do the recruiting except where an associate group searches for a particular talent it needs. The Membership Committee merely guards the door.
          </p>

          <p>
            Club membership is divided into a number of categories, each with it's own quota.
          </p>

          <b>Categories</b>
          <br />
          <ul>
            <li>
              <b>Regular Members (1,000)</b> who live within one hundred miles of the Clubhouse, pay full initiation fees, dues and quarterly minimums, vote, and sponsor candidates for membership.
            </li>
            <br />
            <li>
              <b>Non-Resident Members (500)</b> who live more than one hundred miles from the Clubhouse, pay full initiation fees and dues, but do not vote and cannot sponsor candidates for membership.
            </li>
            <br />
            <li>
              <b>Regular Professional Members (100)</b> who are connected professionally with literature, art, music, or drama, may live anywhere, vote and sponsor candidates for membership, and ordinarily pay full initiation fees and dues (although these changes can be modified by the Board of Directors).
            </li>
            <br />
            <li>
              <b>Associate members (275)</b> who largely make up the chorus, orchestra, band, Jinks Band, and Jinks Material groups, live locally due to rehearsal and performance requirements, pay no initiation fees and reduced dues.
            </li>
            <br />
            <li>
              <b>Non-Resident Associates (25)</b> with special talents who can contribute their talent from a distance, such as writers or composers, or who can perform with little to no rehearsal, pay no initiation fees and reduced dues.
            </li>
            <br />
            <li>
              <b>Faculty Members (127)</b> who are divided into two groups: full professors at the University of California and Stanford University living within one hundred miles of the Clubhouse, plus high administrative officers at the two schools, and all other faculty and faculty-like persons wheresoever residing. If the member leaves academia, he must give up his Faculty membership.
            </li>
            <br />
            <li>
              <b>Military Members (15)</b> who are usually high ranking officers, commonly commanding officers, stationed in the Bay Area, pay reduced initiation fees and dues, and must resign their memberships when they leave the area.
            </li>
            <br />
            <li>
              <b>Consular Members (18)</b> who are invariably local consuls-general, foreign nationals, and members of their country's foreign service, and usually are career diplomats.
            </li>
            <br />
          </ul>
