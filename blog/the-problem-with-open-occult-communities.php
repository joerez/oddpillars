<h4 align="center">By: Joe Rezendes</h4>
    <hr style="width: 50%;"/>




      <div class="container-fluid">
          <p>
            "Drink deep, or taste not the Pierian spring." In Greek mythology, the Pierian Spring of Macedonia was sacred to the Muses. As the metaphorical source of knowledge of art and science, it was popularized by a couplet in Alexander Pope's poem "An Essay on Criticism" : "A little learning is a dang'rous thing; Drink deep, or taste not the Pierian spring."
          </p>


          <p>
            Learning a little bit about something both excites and stupifies you. It is well known that the first thing many people do when they learn something new is to share it with others. The problem with only knowing and understanding a little bit about something is that you become unaware about how much you don't know. Because of this, you'll often see people proclaiming to be Hermeticisits, and Spiritual Gurus. These people all speak of elite past lives, becoming one with everything, claiming heritage to ascended masters, and etc. It's all over the internet, and I can't help but feel embarressed about my own Spiritual Practices due to the thought of being associated with such people.
          </p>

          <p>
            The first issue is that in order to become a knowledgeable occultist, it's nesessary to spend a long period of time going through a rigid process of learning fundamental material. The aspiring occult student usually has no sense of where to go, and is often drawn to these communities as a result. The problem with this is that many of these people are just New Age Spiritualists, and they're not interested in any serious spiritual development. They're interested in whatever makes them feel good. I don't know any serious occultists that post pictures of "deep quotes" in philosophical forums. Yet these platitudes are abundent in nearly all open online communities. To me, it feels like a dumb competition to see who among each other has "the most decalsified pineal gland". There are questions galore ranging from "How do I open my third eye?" to "how do I astral travel?"
          </p>

          <p>
            There are of course different levels of fluff in occult communities. Another one many practioners find annoying is anything and everything about the Kybalion, or the seven hermetic principles. This is probably worth it's own article because of how widespread it is. We all have to start somewhere, but if the only thing you've read is the Kybalion you probably shouldn't be writing about Hermetics.
          </p>

