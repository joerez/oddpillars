    <h1 align="center">The Problem With Open Occult Communities</h1>
    <h3 align="center">
      Many Occultists are turned off by open online Occult Communities for several reasons. <br /><br /> Why is there so much fluff?
    </h3>
    <center>
      <img class="img-responsive" src="https://www.oddpillars.com/images/openproblem.png" />
